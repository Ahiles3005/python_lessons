import pandas as pd

data1 = {
    'Название': ['Доски', 'Цемент', 'Кирпичи', 'Гипсокартон',
                 'Гвозди'],
    'Количество': [100, 50, 2000, 30, 500],
    'Цена': [10.50, 25.00, 0.50, 15.75, 0.10]
}
df1 = pd.DataFrame(data1)
# Создаем вторую таблицу
data2 = {
    'Название': ['Стекло', 'Плитка', 'Двери', 'Краска', 'Шурупы'],
    'Количество': [75, 100, 15, 40, 1000],
    'Цена': [5.25, 12.50, 30.00, 8.99, 0.05]
}
df2 = pd.DataFrame(data2)

df3 = pd.concat([df1,df2],ignore_index=True)
print(df3)

